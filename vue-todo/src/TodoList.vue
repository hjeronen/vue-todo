<script>
    import TodoItem from './TodoItem.vue'
    export default {
        components: {
            TodoItem
        },
        props: {
            todos: {
                type: Array,
                required: true
            }
        },
        methods: {
            handleToggleDone(updatedTodo) {
                this.$emit('toggle-done', updatedTodo);
            }
        }
    }
</script>

<style scoped>
    .list {
        width: 600px;
    }
</style>

<template>
    <div class='list'>
        <ul>
            <TodoItem
                v-for="todo in todos"
                :key="todo.id"
                :todo="todo"
                @toggle-done="handleToggleDone"
            />
        </ul>
    </div>
</template>
